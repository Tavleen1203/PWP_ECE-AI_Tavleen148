#DATA TYPES IN PYTHON

#NUMERIC DATA TYPES
#1.INT
D1=76
print(type(D1))

#2.FLOAT
D2=5.8754
print(type(D2))

#3.COMPLEX
D3=6j
print(type(D3))


#DICTIONARIES
D4={1:'hello',2:'world'}
print(type(D4))


#BOOLEAN
D5=False
print(type(D5))


#SETS
D6={2,3,4}
print(type(D6))


#SEQUENCES
#1.STRING
D8="Python"
print(type(D8))

#2.LIST
D9=[1,2,3,4,5]
print(type(D9))

#3.TUPLES
D10=(1,2,3,4)
print(type(D10))
